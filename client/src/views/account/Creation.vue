                name="passwordConfirm"
                v-model="passwordConfirm"
                :disabled="isLoading"
              />
            </div>

            <div class="mb-2" v-if="recaptchaEnabled">
              <recaptcha
                :sitekey="recaptchaSiteKey"
                @verify="onRecaptchaVerify"
                @expired="onRecaptchaExpired"
